#include <iostream>
#include <string>

using namespace std;
using tp = int64_t;

signed main() {
  tp cnt = 0;
  string s;
  cin >> s;
  for (tp i = 7; i < s.size(); ++i) {
    string x = "";
    for (tp j = 7; ~j; --j) {
      x.push_back(s[i - j]);
    }
    cnt += x == "chuanzhi";
  }
  cout << cnt << '\n';
  return 0;
}

